"use client";

import { useState } from "react";
import { useForm } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";
import { motion, AnimatePresence } from "framer-motion";
import { ref, uploadBytes, getDownloadURL } from "firebase/storage";
import { collection, addDoc, serverTimestamp } from "firebase/firestore";
import { storage, db } from "@/lib/firebase";
import { COLLECTIONS } from "@/lib/types";
import {
  contributionFormSchema,
  type ContributionFormData,
} from "@/lib/contributionSchema";
import type { Gift } from "@/lib/types";

interface ContributionModalProps {
  gift: Gift;
  onClose: () => void;
}

export function ContributionModal({ gift, onClose }: ContributionModalProps) {
  const [success, setSuccess] = useState(false);
  const [submitting, setSubmitting] = useState(false);
  const [submitError, setSubmitError] = useState<string | null>(null);

  const {
    register,
    handleSubmit,
    formState: { errors },
  } = useForm<ContributionFormData>({
    resolver: zodResolver(contributionFormSchema),
    defaultValues: {
      name: "",
      amount: undefined,
      paymentMethod: undefined,
      proof: undefined,
    },
  });

  async function onSubmit(data: ContributionFormData) {
    setSubmitting(true);
    setSubmitError(null);
    try {
      const file = data.proof[0];
      const timestamp = Date.now();
      const safeName = file.name.replace(/[^a-zA-Z0-9.-]/g, "_");
      const path = `wedding_proofs/${timestamp}_${safeName}`;
      const storageRef = ref(storage, path);
      await uploadBytes(storageRef, file);
      const proofImageUrl = await getDownloadURL(storageRef);

      await addDoc(collection(db, COLLECTIONS.contributions), {
        name: data.name.trim(),
        amount: Number(data.amount),
        paymentMethod: data.paymentMethod,
        proofImageUrl,
        status: "pending",
        giftId: gift.id,
        createdAt: serverTimestamp(),
      });

      setSuccess(true);
    } catch (err) {
      setSubmitError(
        err instanceof Error ? err.message : "Error al enviar. Intenta de nuevo."
      );
    } finally {
      setSubmitting(false);
    }
  }

  return (
    <AnimatePresence>
      <motion.div
        className="fixed inset-0 z-50 flex items-center justify-center p-4"
        initial={{ opacity: 0 }}
        animate={{ opacity: 1 }}
        exit={{ opacity: 0 }}
        onClick={onClose}
      >
        <div
          className="absolute inset-0 bg-wedding-deep/50"
          aria-hidden
        />
        <motion.div
          className="relative w-full max-w-lg max-h-[90vh] overflow-y-auto bg-wedding-cream rounded-2xl shadow-xl"
          initial={{ scale: 0.95, opacity: 0 }}
          animate={{ scale: 1, opacity: 1 }}
          exit={{ scale: 0.95, opacity: 0 }}
          transition={{ duration: 0.2 }}
          onClick={(e) => e.stopPropagation()}
        >
          <div className="p-6 sm:p-8">
            <div className="flex justify-between items-start mb-6">
              <h3 className="font-display text-xl text-wedding-deep">
                Aportar a: {gift.name}
              </h3>
              <button
                type="button"
                onClick={onClose}
                className="text-wedding-gray hover:text-wedding-deep transition-colors"
                aria-label="Cerrar"
              >
                <span className="text-2xl leading-none">&times;</span>
              </button>
            </div>

            {success ? (
              <div className="text-center py-8">
                <p className="font-body text-wedding-deep text-lg">
                  Gracias por tu aporte 
                </p>
                <p className="font-body text-wedding-gray mt-2">
                  Validaremos tu comprobante pronto.
                </p>
                <button
                  type="button"
                  onClick={onClose}
                  className="mt-6 rounded-xl bg-wedding-deep text-wedding-cream font-body font-medium px-6 py-2 hover:bg-wedding-soft transition-colors"
                >
                  Cerrar
                </button>
              </div>
            ) : (
              <>
                <p className="font-body text-wedding-gray mb-4">
                  Puedes realizar tu aporte mediante:
                </p>
                <ul className="bg-wedding-beige/50 rounded-xl p-4 mb-6 space-y-3 font-body text-wedding-deep text-sm">
                  <li>
                    <strong>Yape Sof铆a:</strong> 955170938
                  </li>
                  <li>
                    <strong>Yape Luis:</strong> 957329602
                  </li>
                  <li>
                    <strong>Interbank Soles:</strong>
                    <br />
                    N掳 de cuenta: 98742983740347
                    <br />
                    CCI: 00389801327108911445
                  </li>
                  <li>
                    <strong>Interbank d贸lares:</strong>
                    <br />
                    N掳 de cuenta: 2893407312123
                    <br />
                    CCI: 00328901340731212365
                  </li>
                </ul>

                <form
                  onSubmit={handleSubmit(onSubmit)}
                  className="space-y-4"
                >
                  <div>
                    <label
                      htmlFor="name"
                      className="block font-body text-sm font-medium text-wedding-deep mb-1"
                    >
                      Nombre
                    </label>
                    <input
                      id="name"
                      type="text"
                      {...register("name")}
                      className="w-full rounded-xl border border-wedding-gray/30 bg-white px-4 py-2 font-body text-wedding-deep focus:border-wedding-soft focus:outline-none focus:ring-1 focus:ring-wedding-soft"
                      placeholder="Tu nombre"
                    />
                    {errors.name && (
                      <p className="mt-1 text-sm text-red-600">
                        {errors.name.message}
                      </p>
                    )}
                  </div>

                  <div>
                    <label
                      htmlFor="amount"
                      className="block font-body text-sm font-medium text-wedding-deep mb-1"
                    >
                      Monto depositado (S/)
                    </label>
                    <input
                      id="amount"
                      type="number"
                      step="0.01"
                      min="0"
                      {...register("amount", { valueAsNumber: true })}
                      className="w-full rounded-xl border border-wedding-gray/30 bg-white px-4 py-2 font-body text-wedding-deep focus:border-wedding-soft focus:outline-none focus:ring-1 focus:ring-wedding-soft"
                      placeholder="0.00"
                    />
                    {errors.amount && (
                      <p className="mt-1 text-sm text-red-600">
                        {errors.amount.message}
                      </p>
                    )}
                  </div>

                  <div>
                    <label
                      htmlFor="paymentMethod"
                      className="block font-body text-sm font-medium text-wedding-deep mb-1"
                    >
                      M茅todo usado
                    </label>
                    <select
                      id="paymentMethod"
                      {...register("paymentMethod")}
                      className="w-full rounded-xl border border-wedding-gray/30 bg-white px-4 py-2 font-body text-wedding-deep focus:border-wedding-soft focus:outline-none focus:ring-1 focus:ring-wedding-soft"
                    >
                      <option value="">Elige una opci贸n</option>
                      <option value="Yape Sof铆a">Yape Sof铆a</option>
                      <option value="Yape Luis">Yape Luis</option>
                      <option value="Interbank Soles">Interbank Soles</option>
                      <option value="Interbank d贸lares">Interbank d贸lares</option>
                    </select>
                    {errors.paymentMethod && (
                      <p className="mt-1 text-sm text-red-600">
                        {errors.paymentMethod.message}
                      </p>
                    )}
                  </div>

                  <div>
                    <label
                      htmlFor="proof"
                      className="block font-body text-sm font-medium text-wedding-deep mb-1"
                    >
                      Comprobante de pago
                    </label>
                    <input
                      id="proof"
                      type="file"
                      accept="image/*"
                      {...register("proof")}
                      className="w-full rounded-xl border border-wedding-gray/30 bg-white px-4 py-2 font-body text-wedding-deep text-sm file:mr-2 file:rounded-lg file:border-0 file:bg-wedding-soft file:px-3 file:py-1 file:text-wedding-deep"
                    />
                    {errors.proof && (
                      <p className="mt-1 text-sm text-red-600">
                        {errors.proof.message}
                      </p>
                    )}
                  </div>

                  {submitError && (
                    <p className="text-sm text-red-600">{submitError}</p>
                  )}

                  <div className="flex gap-3 pt-2">
                    <button
                      type="button"
                      onClick={onClose}
                      className="flex-1 rounded-xl border border-wedding-gray/40 text-wedding-deep font-body font-medium py-3 hover:bg-wedding-beige/50 transition-colors"
                    >
                      Cancelar
                    </button>
                    <button
                      type="submit"
                      disabled={submitting}
                      className="flex-1 rounded-xl bg-wedding-deep text-wedding-cream font-body font-medium py-3 hover:bg-wedding-soft transition-colors disabled:opacity-60"
                    >
                      {submitting ? "Enviando..." : "Enviar aporte"}
                    </button>
                  </div>
                </form>
              </>
            )}
          </div>
        </motion.div>
      </motion.div>
    </AnimatePresence>
  );
}
